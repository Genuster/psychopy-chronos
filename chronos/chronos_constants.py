"""PST Chronos device constants and initialization sequence."""

# --- USB identifiers ---
VENDOR_ID  = 0x2266  # USB-IF vendor ID assigned to PST (Psychology Software Tools)
PRODUCT_ID = 0x0007  # PST product ID for the Chronos response box (PST-100430)

# --- Button event endpoint (EP 0x82) ---
EP_IN = 0x82  # bulk IN endpoint for button events
PKT_LEN = 20  # expected minimum packet length
READ_SIZE = 64  # max bytes per USB read
TIMEOUT_MS = 10  # read timeout before retry (ms)

# --- Byte offsets inside a single 10-byte button event slot ---
_OFF_TS = 2  # 4-byte big-endian µs timestamp
_OFF_BUTTON_LEVEL = 7    # button state bitmask: set while held, clear on release
_OFF_BUTTON_CHANGED = 9  # button event bitmask: bits that changed this packet

# --- Second event slot starts at byte 11 ---
_SLOT2_BASE = 11  # each packet can carry two 10-byte event slots
_OFF_TS_2 = _SLOT2_BASE + 2             # bytes 13-16
_OFF_BUTTON_LEVEL_2 = _SLOT2_BASE + 6   # byte 17
_OFF_BUTTON_CHANGED_2 = _SLOT2_BASE + 8 # byte 19

# --- Byte offsets for AUX inputs inside a single 10-byte event slot ---
# Physical buttons use bytes 7/9 (state/event); AUX inputs use bytes 6/8.
_OFF_AUX_LEVEL   = 6                    # slot 1: AUX line level  (G=0x80, F=0x40)
_OFF_AUX_CHANGED = 8                    # slot 1: AUX inputs that changed
_OFF_AUX_LEVEL_2   = _SLOT2_BASE + 5   # slot 2: AUX line level   (byte 16)
_OFF_AUX_CHANGED_2 = _SLOT2_BASE + 7   # slot 2: AUX inputs that changed (byte 18)

# --- Button bitmask → label ---
BUTTON_MAP: list[tuple[int, str]] = [
    (0x01, "1"),  # Button 1 (leftmost)
    (0x02, "2"),  # Button 2
    (0x04, "3"),  # Button 3 (centre)
    (0x08, "4"),  # Button 4
    (0x10, "5"),  # Button 5 (rightmost)
]

# --- AUX input bitmask → label ---
AUX_MAP: list[tuple[int, str]] = [
    (0x40, "F"),  # IN15, AUX connector pin 7 (brown wire)
    (0x80, "G"),  # IN16, AUX connector pin 6 (yellow wire)
]

# --- LED command endpoint (EP 0x01 / EP 0x81) ---
EP_CMD_OUT            = 0x01   # LED/init command endpoint (host → device)
EP_CMD_IN             = 0x81   # LED/init ack endpoint    (device → host)
_CMD_WRITE_TIMEOUT_MS = 100    # write timeout for LED/init commands (ms);
                               # USB interrupt transfer completes in ~1ms,
                               # 100ms gives headroom while failing fast on stall

# Number of LEDs on the device
_NUM_LEDS = 5

# LED command packet ID (first byte of every cmd=0x00 LED packet)
_CMD_LED = 0x00

# LED op codes (byte 0 of each (op, reg, val) triplet)
_OP_WRITE   = 0x83  # pre-load a color register value
_OP_ENABLE  = 0x81  # enable  LED (reg=_REG_ENABLE, val=per-LED bitmask)
_OP_DISABLE = 0x80  # disable LED (reg=_REG_ENABLE, val=per-LED bitmask)

# LED control register: each bit enables/disables the corresponding LED
_REG_ENABLE = 0xC6

# Per-LED color registers.  Index = LED position (0 = leftmost, 4 = rightmost).
_LED_REG_R = [0xF0, 0xF1, 0xF2, 0xF3, 0xF4]
_LED_REG_G = [0xF5, 0xF6, 0xF7, 0xF8, 0xF9]
_LED_REG_B = [0xFA, 0xFB, 0xFC, 0xFD, 0xFE]

# Per-LED bitmasks for _REG_ENABLE (bit i controls LED i)
_LED_BITS = [0x01, 0x02, 0x04, 0x08, 0x10]

# Calibration: device returns per-LED R/G/B scale factors in response to cmd=0x1b.
# The response begins with 0x1b, followed by 2 header bytes, then IEEE 754 floats.
_CMD_CALIB         = 0x1B   # calibration read command; echoed as first byte of response
_CALIB_RESP_OFFSET = 3      # bytes to skip before the float payload in the response
_CALIB_COUNT       = 15     # expected coefficients: R/G/B × 5 LEDs
_CALIB_FLOAT_MIN   = 0.05   # filter: reject subnormal/near-zero floats
_CALIB_FLOAT_MAX   = 1.0    # filter: valid coefficients are 0–1 scale factors
_CALIB_WAIT_S      = 2.0    # max seconds to wait for calibration data during init

# Commit command (0x1c) response: first 3 bytes echo cmd/seq/0x00, then 4 descriptor bytes.
# Captured value: 01 00 21 00; consistent across all commits; likely firmware version.
# Interpreted as two little-endian uint16: version=1, build=33 (0x21).
_CMD_COMMIT        = 0x1C   # commit/apply command; response carries firmware descriptor
_COMMIT_RESP_LEN   = 7      # expected response length (3-byte header + 4-byte descriptor)
_FW_DESC_OFFSET    = 3      # byte offset of the 4-byte descriptor in the commit response

# LED sequence counter: wraps 0x00–0xFF; device does not enforce order
_LED_SEQ_INIT = 0xA0  # arbitrary starting value matching E-Prime's observed behaviour

# Drain thread: short poll so it reacts within ~1 ms when an ack is pending
_LED_DRAIN_TIMEOUT_MS = 5

